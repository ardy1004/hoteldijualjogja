import { z } from "zod";
import { insertInquirySchema } from "./schema";

export const errorSchemas = {
  validation: z.object({
    message: z.string(),
    field: z.string().optional(),
  }),
  internal: z.object({
    message: z.string(),
  }),
};

export const api = {
  inquiries: {
    create: {
      method: "POST" as const,
      path: "/api/inquiries" as const,
      input: insertInquirySchema,
      responses: {
        201: z.object({
          success: z.boolean(),
          whatsappUrl: z.string(),
        }),
        400: errorSchemas.validation,
      },
    },
  },
};

export function buildUrl(
  path: string,
  params?: Record<string, string | number>
): string {
  let url = path;
  if (params) {
    Object.entries(params).forEach(([key, value]) => {
      if (url.includes(`:${key}`)) {
        url = url.replace(`:${key}`, String(value));
      }
    });
  }
  return url;
}

export type CreateInquiryInput = z.infer<typeof api.inquiries.create.input>;
export type CreateInquiryResponse = z.infer<
  typeof api.inquiries.create.responses[201]
>;
